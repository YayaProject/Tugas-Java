public class operator_inkrement_dekrement {

    public static void main(String[] args) {
        int a = 5;

        // Menggunakan pre-increment
        System.out.println("Pre-increment: " + ++a); // Output: 6
        // Nilai a sekarang adalah 6

        // Menggunakan post-increment
        System.out.println("Post-increment: " + a++); // Output: 6
        // Nilai a setelah ini menjadi 7

        // Menampilkan nilai akhir a
        System.out.println("Nilai akhir a: " + a); // Output: 7
    }
}
