public class operator {
    public static void main(String[] args) {
          // Operator Aritmatika
        int a = 10;
        int b = 5;
        int sum = a + b;           // Penjumlahan
        int difference = a - b;    // Pengurangan
        int product = a * b;       // Perkalian
        int quotient = a / b;      // Pembagian
        int remainder = a % b;     // Modulus

        // Operator Increment/Decrement
        int x = 10;
        x++;  // Increment
        x--;  // Decrement
       
        // Operator Perbandingan
        boolean isEqual = (a == b);           // Sama dengan
        boolean isNotEqual = (a != b);        // Tidak sama dengan
        boolean isGreater = (a > b);          // Lebih besar dari
        boolean isLessOrEqual = (a <= b);     // Lebih kecil atau sama dengan
    }
    
}

